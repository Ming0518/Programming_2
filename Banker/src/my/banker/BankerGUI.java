    private void viewTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewTFActionPerformed
        banker.ListBanker listBanker = new banker.ListBanker();
        banker.NodeBanker current = listBanker.getHead();
        banker.Banker displayBanker;

        jTextArea.setText(" ");
        for (int i = 0; i < count; i++) {
            if (current != null) {
                displayBanker = current.getBanker();
                jTextArea.append(i+1+". ");
                jTextArea.append("ID: "+displayBanker.getId());
                jTextArea.append("\tName: "+displayBanker.getName());
                jTextArea.append("\tEmail: "+displayBanker.getEmail());
                jTextArea.append("\tPhoneNumber: "+displayBanker.getPhoneNumber());
                jTextArea.append("\tWorkExperience: "+displayBanker.getWorkExperience()+"\n ");
                current = current.getNext();
            }
        }
    }//GEN-LAST:event_viewTFActionPerformed


    private void SearchTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchTFActionPerformed
        String selectedID = idComboBox.getSelectedItem().toString();
        idTF.setText(selectedID);
        
        banker.ListBanker listBanker = new banker.ListBanker();
        banker.NodeBanker current = listBanker.getHead();
        
        while (current != null) {
            if (current.getBanker().getId().equals(selectedID)) {
                banker.Banker selectedBanker = current.getBanker();
                nameTF.setText(selectedBanker.getName());
                emailTF.setText(selectedBanker.getEmail());
                phoneNumberTF.setText(String.valueOf(selectedBanker.getPhoneNumber()));
                WorkExperienceTF.setText(String.valueOf(selectedBanker.getWorkExperience()));
                return;
            }
            current = current.getNext();
        }
       
    }//GEN-LAST:event_SearchTFActionPerformed


    

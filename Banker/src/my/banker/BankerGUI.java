    private void updateTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTFActionPerformed
        String idText = idTF.getText(); 

        
        banker.ListBanker listBanker = new banker.ListBanker();
        NodeBanker currentNode = listBanker.getHead();
        Banker bankerUpdate = null;

        while (currentNode != null) {
            Banker currentBanker = currentNode.getBanker();
            if (currentBanker.getId().equals(idText)) {
                bankerUpdate = currentBanker;
                break;
            }
            currentNode = currentNode.getNext();
        }

        if (bankerUpdate != null) {
            String nameText = nameTF.getText();
            String emailText = emailTF.getText();
            String phoneNumberText = phoneNumberTF.getText();
            String workExperienceText = WorkExperienceTF.getText();
            
            int workExperience = Integer.parseInt(workExperienceText);

            bankerUpdate.setName(nameText);
            bankerUpdate.setEmail(emailText);
            bankerUpdate.setPhoneNumber(phoneNumberText);
            bankerUpdate.setWorkExperience(workExperience);
            
            jTextArea.append("Update Successful\n");

        } else {
            jTextArea.append("ID Undefined\n");
        }
    }//GEN-LAST:event_updateTFActionPerformed

    private void addTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTFActionPerformed
    String idText = idTF.getText();
    String nameText = nameTF.getText();
    String emailText = emailTF.getText();
    String phoneNumberText = phoneNumberTF.getText();
    String workExperienceText = WorkExperienceTF.getText();
    
    int workExperience = Integer.parseInt(workExperienceText);

    banker.Banker newBanker = new banker.Banker(idText , nameText, emailText, phoneNumberText, workExperience);
    
    banker.ListBanker.add(newBanker); 
    jTextArea.append("Data Save Successful\n");
    idComboBox.addItem(idText);
    count += 1;

    }//GEN-LAST:event_addTFActionPerformed

